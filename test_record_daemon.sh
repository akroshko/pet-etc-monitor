#!/bin/bash

CONTINUE=1
send_signal_to_process () {
    echo "Terminating from record daemon script upon user SIGINT"
    CONTINUE=
}
trap send_signal_to_process SIGINT

if ! pkill -SIGTERM -f 'test_record_daemon.py' &>/dev/null; then
    if [[ "$@" != *--no-start* ]]; then
        ./exec_venv.sh ./test_record_daemon.py 2>&1 | (trap '' SIGINT; tee test_record_daemon.out)
        while [[ -n "$CONTINUE" ]]; do
            sleep 2
            {
                for i in $(seq 1 20); do
                    echo ""
                done
                echo "================================================================================================================================================================";
                echo "================================================================================================================================================================";
                echo "==== Record Daemon Restart =====================================================================================================================================";
                echo "================================================================================================================================================================";
                echo "================================================================================================================================================================";
                ./exec_venv.sh ./test_record_daemon.py 2>&1
            } | (trap '' SIGINT; tee test_record_daemon.out)
        done
    fi
fi
